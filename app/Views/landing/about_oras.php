<!-- Blog-Style History Section with Images -->
<div class="stats bg-light">
    <div class="container">
        <div class="row justify-content-center">
            <div class="col-lg-10">
                <div class="card border-0 shadow-sm">
                    <div class="card-body">
                        <h2 class="text-center mb-4">A History of Oras: Its Formation as a Pueblo (Municipality)</h2>
                        <hr>
                        <!-- Paragraph 1 (image left, text right) -->
                        <div class="blog-section mb-4">
                            <div class="row align-items-center">
                                <div class="col-md-4 mb-3 mb-md-0">
                                    <img src="public/dist/landing/images/about-oras/about-oras-1.jpg" class="img-fluid rounded" alt="Early Oras settlement">
                                </div>
                                <div class="col-md-8">
                                    <p>
                                        Not long after Miguel Lopez de Legaspi anchored in Tubabao island, Orás, on February 13, 1565 and eventually established the seat of his government in Manila, he parceled out the entire archipelago among encomenderos; the tiny hamlets near Orás river fell under the encomienda of Tubig and Sulat. The villagers' first significant encounter with Spanish culture came through Jesuit priests who founded a missionary base on Bacod (formerly a bungto, now part of Dolores river bed) in the late 1590s. From Bacod, the Jesuit minister often visited the hamlets along Orás river and the seashore, introducing their inhabitants to the rudiments of the Catholic faith and helping concentrate these hamlets into larger villages, as well as defend themselves from Muslim raids.
                                    </p>
                                </div>
                            </div>
                        </div>
                        <!-- Paragraph 2 (image right, text left) -->
                        <div class="blog-section mb-4">
                            <div class="row align-items-center flex-md-row-reverse">
                                <div class="col-md-4 mb-3 mb-md-0">
                                    <img src="public/dist/landing/images/about-oras/about-oras-2.jpg" class="img-fluid rounded" alt="Franciscan missionaries">
                                </div>
                                <div class="col-md-8">
                                    <p>
                                        When the Franciscan missionaries took over from the Jesuits in 1768, they continued the program of <em>reduccion</em>, encouraging people to settle in the village of Orás from scattered hamlets so that faith, culture, and governance could be more easily fostered. By the early 1800s, Orás was already recognized as a <em>visita</em> (barrio) under the jurisdiction of Paric (now Dolores). It had its own <em>tenientes</em> and <em>alguaciles</em> governing the natives. A letter dated June 20, 1844, from Francisco Castilla, gobernador politico-militar de Samar, mentions “near the visita of Orás, jurisdiction of Paric, nine vessels and nine balotos of Moros docked” on September 13, 1843.
                                    </p>
                                </div>
                            </div>
                        </div>
                        <!-- Paragraph 3 (image left, text right) -->
                        <div class="blog-section mb-4">
                            <div class="row align-items-center">
                                <div class="col-md-4 mb-3 mb-md-0">
                                    <img src="public/dist/landing/images/about-oras/about-oras-3.jpg" class="img-fluid rounded" alt="Church construction">
                                </div>
                                <div class="col-md-8">
                                    <p>
                                        The formation of Orás as a pueblo is largely credited to the zeal of the parish priests of Tubig (Taft), who organized the town and built a stone church, a large plaza, a convento, a walled cemetery, and a tribunal. Father Manuel Lozano, parish priest of Tubig, led the construction of a stone church (41.4 m long, 13.2 m wide), which unfortunately was demolished in 1963. In 1886, there was a wooden convento on the site now occupied by the Holy Cross Academy, measuring 10 m by 9.5 m. Churches, rectories, and plazas were placed at the center, with prominent men allowed to build homes around the complex.
                                    </p>
                                </div>
                            </div>
                        </div>
                        <!-- Paragraph 4 (image right, text left) -->
                        <div class="blog-section mb-4">
                            <div class="row align-items-center flex-md-row-reverse">
                                <div class="col-md-4 mb-3 mb-md-0">
                                    <img src="public/dist/landing/images/about-oras/about-oras-4.jpg" class="img-fluid rounded" alt="Pueblo officials">
                                </div>
                                <div class="col-md-8">
                                    <p>
                                        As infrastructures required for pueblo status were built and the Christianized population could now support a parish priest, Orás' separation as a pueblo from Paric was recommended and approved by the Governor-General on December 13, 1849. Its establishment as a parish was declared on August 31, 1863. Officials included gobernadorcillo, teniente primero, teniente segundo, juez, and alguaciles. By the 1890s, these included teniente mayor, teniente de policia, teniente de semetera, and teniente de ganados, as well as a principalia (the town’s aristocracy).
                                    </p>
                                </div>
                            </div>
                        </div>
                        <!-- Paragraph 5 (image left, text right) -->
                        <div class="blog-section mb-4">
                            <div class="row align-items-center">
                                <div class="col-md-4 mb-3 mb-md-0">
                                    <img src="public/dist/landing/images/about-oras/about-oras-5.jpg" class="img-fluid rounded" alt="First parish priests">
                                </div>
                                <div class="col-md-8">
                                    <p>
                                        Due to a shortage of Franciscan missionaries, it was only on May 18, 1864, that Orás was approved as a parish under St. Peter of Alcantara. Initially, it was still serviced by the Tubig parish priest. On June 23, 1870, Fr. Fernando Esteban became the first parish priest, arriving in 1875 and serving until 1879. He was succeeded by Fr. Bernardo Tapiol (1879), Fr. Ildefonso Dominguez (1884), Fr. Ramon Amoros (1885), Fr. Candido Esguerra (1890), and Fr. Eustaquio Paniagua (1896), who served until the Revolution.
                                    </p>
                                </div>
                            </div>
                        </div>
                        <!-- Paragraph 6 (image right, text left) -->
                        <div class="blog-section mb-4">
                            <div class="row align-items-center flex-md-row-reverse">
                                <div class="col-md-4 mb-3 mb-md-0">
                                    <img src="public/dist/landing/images/about-oras/about-oras-6.jpg" class="img-fluid rounded" alt="Agriculture and schools">
                                </div>
                                <div class="col-md-8">
                                    <p>
                                        The people of Oras engaged in agriculture and fishing, planting rice, coconut, abaca, and palawan. Products such as coconut oil, abaca cloth, rice, and dried fish were transported to Catbalogan via Ulot river and other towns. Fr. Esteban initiated construction of a road to Paric. Chinese merchants dominated commerce in the poblacion in the late 1800s. Fr. Esguerra, even before his official appointment, built two schools and created canals to prevent flooding. A primary school had already been established by the Franciscans in the 1860s.
                                    </p>
                                </div>
                            </div>
                        </div>
                        <!-- Paragraph 7 (image left, text right) -->
                        <div class="blog-section mb-4">
                            <div class="row align-items-center">
                                <div class="col-md-4 mb-3 mb-md-0">
                                    <img src="public/dist/landing/images/about-oras/about-oras-7.jpg" class="img-fluid rounded" alt="Oras map and barrios">
                                </div>
                                <div class="col-md-8">
                                    <p>
                                        Known as Yorás in Spanish documents, the town is located at 12°9' latitude on the east coast of Samar. Originally, it was bounded by Palapag (north), Catubig and mountains (west), Paric (south), and the Pacific Ocean (east). By 1859, noted barrios included Japonan, Jambo, Alibangbang, and Gamay. In the late 1880s, Gamay and Lapinig came under the parish of Palapag, while Binalhag, San Eduardo, Concepcion (Binuangan), and Tangbo were raised to <em>visita</em> status. Tubabao island was originally part of Dolores. The town’s population was 3,028 in 1864 and 4,527 in 1884. Many died in cholera epidemics in the late 1800s, especially in 1883 when Oras had the tenth highest mortality on Samar island.
                                    </p>
                                </div>
                            </div>
