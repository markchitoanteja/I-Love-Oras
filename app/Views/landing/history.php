                                <div class="col-md-8">
                                    <p>
                                        Due to a shortage of Franciscan missionaries, it was only on May 18, 1864, that Orás was approved as a parish under St. Peter of Alcantara. Initially, it was still serviced by the Tubig parish priest. On June 23, 1870, Fr. Fernando Esteban became the first parish priest, arriving in 1875 and serving until 1879. He was succeeded by Fr. Bernardo Tapiol (1879), Fr. Ildefonso Dominguez (1884), Fr. Ramon Amoros (1885), Fr. Candido Esguerra (1890), and Fr. Eustaquio Paniagua (1896), who served until the Revolution.
                                    </p>
