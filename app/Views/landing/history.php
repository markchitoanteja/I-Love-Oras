                                    <p>
                                        Not long after Miguel Lopez de Legaspi anchored in Tubabao island, Orás, on February 13, 1565 and eventually established the seat of his government in Manila, he parceled out the entire archipelago among encomenderos; the tiny hamlets near Orás river fell under the encomienda of Tubig and Sulat. The villagers' first significant encounter with Spanish culture came through Jesuit priests who founded a missionary base on Bacod (formerly a bungto, now part of Dolores river bed) in the late 1590s. From Bacod, the Jesuit minister often visited the hamlets along Orás river and the seashore, introducing their inhabitants to the rudiments of the Catholic faith and helping concentrate these hamlets into larger villages, as well as defend themselves from Muslim raids.
