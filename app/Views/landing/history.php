                                <div class="col-md-8">
                                    <p>
                                        As infrastructures required for pueblo status were built and the Christianized population could now support a parish priest, Orás' separation as a pueblo from Paric was recommended and approved by the Governor-General on December 13, 1849. Its establishment as a parish was declared on August 31, 1863. Officials included gobernadorcillo, teniente primero, teniente segundo, juez, and alguaciles. By the 1890s, these included teniente mayor, teniente de policia, teniente de semetera, and teniente de ganados, as well as a principalia (the town’s aristocracy).
                                    </p>
