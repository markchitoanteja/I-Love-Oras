                                <div class="col-md-8">
                                    <p>
                                        The formation of Orás as a pueblo is largely credited to the zeal of the parish priests of Tubig (Taft), who organized the town and built a stone church, a large plaza, a convento, a walled cemetery, and a tribunal. Father Manuel Lozano, parish priest of Tubig, led the construction of a stone church (41.4 m long, 13.2 m wide), which unfortunately was demolished in 1963. In 1886, there was a wooden convento on the site now occupied by the Holy Cross Academy, measuring 10 m by 9.5 m. Churches, rectories, and plazas were placed at the center, with prominent men allowed to build homes around the complex.
                                    </p>
